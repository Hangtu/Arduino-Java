    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // saber si existe algunos elementos dentro de la palabra
        String texto= textArea1.getText();
        String primer=showInputDialog("Que Buscas");
        int segun=Integer.parseInt(showInputDialog(this,"desde que indice"));
        int valor=texto.indexOf(primer, segun);
        if(valor>=0){
            showMessageDialog(this,"Si Existe Eso Dentro La Cadena");
        }
        else
            showMessageDialog(this,"No Existe Eso Dentro De La Cadena");
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
         String texto= textArea1.getText();
         try{
         String primer=showInputDialog("Con Que Termina La Palabra");
           boolean resultado=texto.endsWith(primer);
         if(resultado==true){
             showMessageDialog(this,"Exacto");
         }
         if (resultado==false){
             showMessageDialog(this,"Falso");
         }
          
         }
         catch(java.lang.Exception e){
            System.out.print("Cancelado");
         }
          
    }//GEN-LAST:event_jButton8ActionPerformed

